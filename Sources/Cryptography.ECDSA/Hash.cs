            var index = 0;
            while (outlen > 0)
            {
                secp256k1_hmac_sha256_t hmac = new secp256k1_hmac_sha256_t();
                int now = outlen;
                secp256k1_hmac_sha256_initialize(hmac, rng.k, 32);
