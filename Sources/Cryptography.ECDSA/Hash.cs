        private static void secp256k1_hmac_sha256_initialize(secp256k1_hmac_sha256_t hash, byte[] key, UInt32 keylen)
        {
            var rkey = new byte[64];
            if (keylen <= 64)
            {
                Util.Memcpy(key, 0, rkey, 0, keylen);//memcpy(rkey, key, keylen);
                Util.MemSet(rkey, keylen, 0, 64 - keylen);//memset(rkey + keylen, 0, 64 - keylen);
            }
            else
            {
                secp256k1_sha256_t sha256 = new secp256k1_sha256_t();
                secp256k1_sha256_initialize(sha256);
                secp256k1_sha256_write(sha256, key, keylen);
                secp256k1_sha256_finalize(sha256, rkey);
                Util.MemSet(rkey, 32, 0, 32); //memset(rkey + 32, 0, 32);
            }

            int n;
            secp256k1_sha256_initialize(hash.outer);
            for (n = 0; n < 64; n++)
            {
                rkey[n] ^= 0x5c;
            }
            secp256k1_sha256_write(hash.outer, rkey, 64);

            secp256k1_sha256_initialize(hash.inner);
            for (n = 0; n < 64; n++)
            {
                rkey[n] ^= 0x5c ^ 0x36;
            }
            secp256k1_sha256_write(hash.inner, rkey, 64);
            Util.MemSet(rkey, 0, 64);//memset(rkey, 0, 64);
        }

        private static void secp256k1_hmac_sha256_write(secp256k1_hmac_sha256_t hash, byte[] data, UInt32 size)
        {
            secp256k1_sha256_write(hash.inner, data, size);
        }

